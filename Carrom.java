class Carrom{
	
	String material;
	String shape;
	String brand;
	int noOfPlayers;
	int price;
	
	
	Carrom (){
		System.out.println("invoking no arguments Carrom");
		System.out.println("Material :" + this.material);
		System.out.println("Shape :" + this.shape);
		System.out.println("Brand :" + this.brand);
		System.out.println("No Of Players :" + this.noOfPlayers);
		System.out.println("Price :" + this.price);
		System.out.println("\n");
	}
	
	
	Carrom(String material)
	
	{

		System.out.println("invoking in Carrom");
		
	
		this.material=material;
		this.shape=shape;
		this.brand=brand;
		this.noOfPlayers=noOfPlayers;
		this.price=price;
		
	}
	
	Carrom(String material, String shape)
	
	{

		System.out.println("invoking in Carrom");
		
		this.material=material;
		this.shape=shape;
		this.brand=brand;
		this.noOfPlayers=noOfPlayers;
		this.price=price;
		
	}
	
	Carrom(String material, String shape, int noOfPlayers)
	
	{

		System.out.println("invoking in Carrom");
		
		this.material=material;
		this.shape=shape;
		this.brand=brand;
		this.noOfPlayers=noOfPlayers;
		this.price=price;
		
	}
	
	Carrom(String material, String shape, String brand, int noOfPlayers)
	
	{

		System.out.println("invoking in Carrom");
		
		this.material=material;
		this.shape=shape;
		this.brand=brand;
		this.noOfPlayers=noOfPlayers;
		this.price=price;
		
	}
	
	Carrom(String material, String shape, String brand, int noOfPlayers, int price)
	
	{

		System.out.println("invoking in Carrom");
		
		this.material=material;
		this.shape=shape;
		this.brand=brand;
		this.noOfPlayers=noOfPlayers;
		this.price=price;
		
	}
}